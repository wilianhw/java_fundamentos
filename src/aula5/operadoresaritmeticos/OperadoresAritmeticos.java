package aula5.operadoresaritmeticos;

public class OperadoresAritmeticos {
    public static void main(String args[]) {
        int valor1 = 10;
        int valor2 = 5;

        System.out.println("Somatório: " + (valor1 + valor2));
        System.out.println("Subtração: " + (valor1 - valor2));
        System.out.println("Multiplicação: " + (valor1 * valor2));
        System.out.println("Divisão: " + (valor1 / valor2));
        System.out.println("Modulo: " + (valor1 % valor2));
    }
}