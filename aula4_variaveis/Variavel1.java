public class Variavel1 {
    public static void main(String args[]) {
        int quantidade; //Declarando variável
        quantidade = 10; //Atribuindo o valor 10 a variável 10;

        System.out.println(quantidade);

        quantidade = 15; //Atribuindo o valor 15 a variável 15.

        System.out.println(quantidade);
    }
}